x = 10
x += 5   # x = x + 5
print("x += 5 ->", x)
x *= 2   # x = x * 2
print("x *= 2 ->", x)
x -= 10  # x = x - 10
print("x -= 10 ->", x)
x //= 3  # x = x // 3 (floor division)
print("x //= 3 ->", x)
