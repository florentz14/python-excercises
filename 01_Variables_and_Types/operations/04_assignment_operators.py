# Assignment Operators in Python

# Basic assignment
a = 10
print(f"Initial a: {a}")

# Add and assign
a += 5  # a = a + 5
print(f"After a += 5: {a}")

# Subtract and assign
a -= 3  # a = a - 3
print(f"After a -= 3: {a}")

# Multiply and assign
a *= 2  # a = a * 2
print(f"After a *= 2: {a}")

# Divide and assign
a /= 4  # a = a / 4
print(f"After a /= 4: {a}")

# Modulus and assign
a %= 3  # a = a % 3
print(f"After a %= 3: {a}")

# Exponent and assign
a **= 2  # a = a ** 2
print(f"After a **= 2: {a}")

# Floor divide and assign
a //= 2  # a = a // 2
print(f"After a //= 2: {a}")