a = 10
b = 5

print(f"a = {a}, b = {b}")

# Equal to
print(f"a == b: {a == b}")

# Not equal to
print(f"a != b: {a != b}")

# Greater than
print(f"a > b: {a > b}")

# Less than
print(f"a < b: {a < b}")

# Greater than or equal to
print(f"a >= b: {a >= b}")

# Less than or equal to
print(f"a <= b: {a <= b}")

# Comparing strings
str1 = "hello"
str2 = "world"
print(f"'{str1}' == '{str2}': {str1 == str2}")