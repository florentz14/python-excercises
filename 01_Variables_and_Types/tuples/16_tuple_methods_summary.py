# File: 16_tuple_methods_summary.py
# Purpose: Tuple methods summary.
# Description: count(), index(), len(), min(), max().
#              Tuples have few methods because they are immutable.
# ------------------------------------------------------------

t = (3, 1, 4, 1, 5, 9, 2, 6)

print("Tuple:", t)
print("len(t):", len(t))
print("min(t):", min(t))
print("max(t):", max(t))
print("t.count(1):", t.count(1))
print("t.index(5):", t.index(5))
