numbers = [1, 2, 3, 4, 5]

print("Length:", len(numbers))
print("Minimum:", min(numbers))
print("Maximum:", max(numbers))
print("Sum:", sum(numbers))

# With strings
words = ["apple", "banana", "cherry"]
print("Min word:", min(words))
print("Max word:", max(words))
