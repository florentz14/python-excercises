# Descripción: Cambiar el último elemento de la lista por 99. En Python
#              el índice -1 significa "el último elemento".
# ---------------------------------------------------------------------------

# Lista inicial
nums = [1, 2, 3, 4, 5]
# nums[-1] es el último elemento; lo asignamos a 99 (la lista se modifica)
nums[-1] = 99
# Mostramos la lista resultante: [1, 2, 3, 4, 99]
print(nums)
