# Descripción: Unir dos listas en una sola usando el operador + (concatenación).
#              El resultado es una lista nueva con todos los elementos en orden.
# ---------------------------------------------------------------------------

a = [1, 2, 3]
b = [4, 5, 6]
# + concatena las dos listas en una nueva: [1,2,3,4,5,6]
c = a + b
print(c)
