# Lista de frutas
fruits = ['Apple', 'Banana', 'Cherry']
# remove() busca el valor 'Banana' y elimina esa posición de la lista
fruits.remove('Banana')
# Resultado: ['Apple', 'Cherry']
print(fruits)
