# Descripción: Convertir una tupla en lista usando el constructor list().
#              La lista resultante sí se puede modificar.
# ---------------------------------------------------------------------------

# Tupla original (inmutable)
t = ('a', 'b', 'c')
# list() crea una nueva lista con los mismos elementos en el mismo orden
my_list = list(t)
# Mostramos la lista: ['a', 'b', 'c']
print(my_list)
