print("=" * 40)
print("While #1 – Multiples of 3 up to 15")
print("=" * 40)

# Start value
x = 3

# Loop while x is less than or equal to 15
while x <= 15:
    # Print the current value (a multiple of 3)
    print("Value:", x)
    # Increment by 3 to get the next multiple
    x += 3

print("=" * 40)
