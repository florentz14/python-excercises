print("Example 8: Tuple concatenation and repetition")
print("-" * 40)
tuple1 = (1, 2, 3)
tuple2 = (4, 5, 6)
combined = tuple1 + tuple2
print("Tuple 1:", tuple1)
print("Tuple 2:", tuple2)
print("Combined (tuple1 + tuple2):", combined)
repeated = tuple1 * 3
print("Repeated (tuple1 * 3):", repeated)
